const RESERVATION_PATHS = [
  '/dashboard',
  '/reservations',
  '/check-in',
  '/check-out',
  '/planning',
  '/finance',
]

function revalidateReservationPaths() {
  for (const p of RESERVATION_PATHS) {
    revalidatePath(p)
  }
}

